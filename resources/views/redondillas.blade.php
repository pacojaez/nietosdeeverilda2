                <h2 class="p-3 mt-2 text-xl font-bold leading-tight text-gray-200 bg-gray-800 rounded shadow-2xl mix-blend-multiply md:text-3xl bg-opacity-80">
                    REDONDILLAS DEL ADOBE
                </h2>
                <h4 class="p-10 mb-2 text-xs font-bold leading-8 text-gray-200 bg-gray-800 rounded shadow-2xl md:text-lg bg-opacity-80">
                    <span class="italic text-gray-400">
                        Dedicadas a León Felipe, poeta zamorano que cantó a los cantos rodados:
                    </span>
<br>
<br>
<span class="italic text-gray-400">

Como tú que no has servido<br>

Para ser ni piedra<br>

De una lonja,<br>

Ni piedra de una audiencia<br>

Ni piedra de un palacio,<br>

Ni piedra de una iglesia.<br>
</span>

<br>
<br>

1. Barro y paja en la talega<br>

De la arcilla de los campos<br>

Y el agua de los barrancos<br>

Hace adobes en la Vega.<br>

<br>

2. O adobes terracampinos<br>

Para trazar palomares,<br>

Entre negros negrillares,<br>

Cuando asaban palominos.<br>

<br>

3. Se segaba con guadaña,<br>

Se regaba con la noria,<br>

Aún retiene la memoria<br>

El madroño y la cabaña.<br>

<br>

4. O incluso aquel cigüeñal,<br>

Que era artefacto zancudo<br>

Y echaba por un embudo<br>

El agua del manantial.<br>

<br>

5. Laguna de las bodegas,<br>

Rebosante en primavera<br>

Y allí una hermosa pradera<br>

Para descanso en las bregas.<br>

<br>

6. Paja que el burro trajera<br>

Y el barro de la laguna<br>

Pisaban todos a una<br>

Y alisaban la adobera.<br>

<br>

7. Después el sol los secaba,<br>

Como fragua de Vulcano<br>

Y se apilaban a mano<br>

Cuando el astro declinaba.<br>

<br>

8. Es el adobe un invento<br>

De los antiguos sumerios,<br>

Y no encierra otros misterios,<br>

Que del barro hacer cemento.<br>

<br>

9. Los israelitas, legión,<br>

Los amasaban sin ganas,<br>

Cuando en Gosen llovían ranas,<br>

Los magos del Faraón.<br>

<br>

10. Éste les quita la paja,<br>

Pero siguió la exigencia<br>

De buscarla con paciencia<br>

Y el número no rebaja.<br>

<br>

11. ¡Deja ir a nuestra gente!,<br>

Urgió el caudillo Moisés<br>

Y paró Yavé al través<br>

El agua de la corriente.<br>

<br>

12. Siguen columna de nube<br>

O por la noche de fuego<br>

Y el pueblo olvida su ruego<br>

Cuando a la montaña sube.<br>

<br>

13. Mejor el barro del cerro<br>

Que mancharse en los lagares<br>

Con la grasa de olivares<br>

Y luego untar al becerro.<br>

<br>

14. Ya no llegaba del pote<br>

El olor a sus narices<br>

Y llovió Dios codornices<br>

Y cayó de pan un lote.<br>

<br>

15. Con adobe y con tapial<br>

Construyeron sus moradas<br>

Y con tejas coloradas<br>

Puestas sobre el carrizal.<br>

<br>

16. La piedra no era asequible<br>

Salvo para hacer el templo,<br>

Y el barro sirve de ejemplo<br>

De ese ocre inconfundible.<br>

<br>

17. Así astures y vacceos<br>

Poblaron esta región,<br>

Con esta decoración<br>

De pardos sin más rodeos.<br>

<br>

18. De España, esta noble tierra<br>

A California llevó<br>

La técnica, que copió<br>

El gran Junípero Serra.<br>

<br>

19. Y así en la Misión Dolores,<br>

Que es cuna de San Francisco,<br>

Construyó misión y aprisco<br>

Y plantó árboles y flores.<br>

<br>

20. En ciudad de Santa Fe,<br>

En los hoteles de lujo<br>

El adobe se introdujo<br>

Hoy, con su color café.<br>

<br>

21. Si es de tierra roja Adán<br>

Y de una costilla Eva,<br>

De tapial la casa nueva,<br>

Tierra son, tierra serán.<br>

<br>

22. Y ahora aquí en Villaquejida,<br>

En semana cultural,<br>

Se amasa otro barrizal<br>

Y al adobe se da vida.<br>

<br>

23. Si Trajano sin rival<br>

En piedra grabó su gloria,<br>

Quede aquí feliz memoria<br>

En este adobe triunfal.<br>

<br>

24. La piedra leonfelipina<br>

Se recuerda de esta guisa<br>

Y se mancha la camisa<br>

Y se bebe en la cantina.<br>

<br>

25. Si salió arrugado o terso<br>

Válgame Santo Toribio,<br>

Que presto está ya el convivio<br>

Y aquí terminó mi verso.<br>



<br>
(El bardo Ibérico)    <br>
